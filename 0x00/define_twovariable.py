#!/usr/bin/python3
"""a programm that defines two variables a=15 and b=4 perform addition,"
" subtraction, division and multiplication using the print result"""
# Define variables
a = 15
b = 4
# Perform operations and print results
print("Addition:", a, "+", b, "=", a + b)
print("Subtraction:", a, "-", b, "=", a - b)
print("Multiplication:", a, "*", b, "=", a * b)
print("Division:", a, "/", b, "=", a / b)